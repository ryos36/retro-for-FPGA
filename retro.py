#----------------------------------------------------------------
def retro_main( memory:list, io_ports:list ):
  ip = 0
  ext = EXIT
  _stack = [0] * 128
  stack = retro_stack(_stack)
  _address = [0] * 1024
  address = retro_stack(_address)
  ports = [0] * 12
  files = [0] * 8

  opcode = 0
  while ip < ext:
    old_opcode = opcode
    opcode = memory[ip]

    io_ports[DEBUG_PORT0] = ip
    io_ports[DEBUG_PORT1] = opcode
#    print(ip, opcode, stack.top, stack.second);

    # If opcode is not one we know,
    # jump past it. We have 30-opcodes.

    if opcode <= 30:
      if   opcode ==  0:   # nop
        pass

      elif opcode ==  1:   # lit
        ip += 1
        stack.push(memory[ip])

      elif opcode ==  2:   # dup
        stack.push(stack.top)

      elif opcode ==  3:   # drop
        stack.pop()

      elif opcode ==  4:   # swap
        stack.swap()

      elif opcode ==  5:   # push
        address.push(stack.pop())

      elif opcode ==  6:   # pop
        stack.push(address.pop())

      elif opcode ==  7:   # loop
        stack.top -= 1
        stack.sync_top()
        if stack.top != 0 and stack.top > -1:
          ip += 1
          ip = memory[ip] - 1
        else:
          ip += 1
          stack.pop()

      elif opcode ==  8:   # jump
        ip += 1
        ip = memory[ip] - 1

      elif opcode ==  9:   # return
        ip = address.pop()

      elif opcode == 10:   # >= jump
        ip += 1
        a = stack.pop()
        b = stack.pop()
        if b > a:
          ip = memory[ip] - 1

      elif opcode == 11:   # <= jump
        ip += 1
        a = stack.pop()
        b = stack.pop()
        if b < a:
          ip = memory[ip] - 1

      elif opcode == 12:   # != jump
        ip += 1
        a = stack.pop()
        b = stack.pop()
        if b != a:
          ip = memory[ip] - 1

      elif opcode == 13:   # == jump
        ip += 1
        a = stack.pop()
        b = stack.pop()
        if b == a:
          ip = memory[ip] - 1

      elif opcode == 14:   # @
        stack.top = memory[stack.top]
        stack.sync_top()

      elif opcode == 15:   # !
        mi = stack.pop()
        memory[mi] = stack.pop()

      elif opcode == 16:   # +
        t = stack.pop()
        stack.top += t
        stack.sync_top()

      elif opcode == 17:   # -
        t = stack.pop()
        stack.top -= t
        stack.sync_top()

      elif opcode == 18:   # *
        t = stack.pop()
        stack.top *= t
        stack.sync_top()

      elif opcode == 19:   # /mod
        (stack.top, stack.second) = retro_divmod(stack.second, stack.top)
        stack.sync()

      elif opcode == 20:   # and
        t = stack.pop()
        stack.top &= t
        stack.sync_top()

      elif opcode == 21:   # or
        t = stack.pop()
        stack.top |= t
        stack.sync_top()

      elif opcode == 22:   # xor
        t = stack.pop()
        stack.top ^= t
        stack.sync_top()

      elif opcode == 23:   # <<
        t = stack.pop()
        stack.top <<= t
        stack.sync_top()

      elif opcode == 24:   # >>
        t = stack.pop()
        stack.top >>= t
        stack.sync_top()

      elif opcode == 25:   # 0;
        if stack.top == 0:
          stack.pop()
          ip = address.pop()

      elif opcode == 26:   # inc
        stack.top += 1
        stack.sync_top()

      elif opcode == 27:   # dec
        stack.top -= 1
        stack.sync_top()

      elif opcode == 28:   # in
        t = stack.top
        stack.top = ports[t]
        ports[t] = 0
        stack.sync_top()

      elif opcode == 29:   # out
        pi = stack.pop()
        ports[ pi ] = stack.pop()

      elif opcode == 30:   # wait
        # Only call if we have pending I/O
        if ports[0] == 0:
          ip = handle_devices( ip, stack, address, ports, memory, io_ports)

    else:
      address.push(ip)
      ip = memory[ip] - 1

    #assert ( stack.top != None ) 
    ip += 1

